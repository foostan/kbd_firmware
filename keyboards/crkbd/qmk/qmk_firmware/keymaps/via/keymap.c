static uint16_t key_timer;
static void refresh_rgb(void); 
static void check_rgb_timeout(void);
bool is_rgb_timeout = false;

void oled_sync_handler(uint8_t in_buflen, const void* in_data, uint8_t out_buflen, void* out_data) {
    memcpy(&is_rgb_timeout, in_data, in_buflen);
}

void keyboard_post_init_user(void) {
    transaction_register_rpc(RPC_OLED_SYNC, oled_sync_handler);
}

void refresh_rgb() {
  key_timer = timer_read(); 
  if (is_rgb_timeout) { 
    is_rgb_timeout = false;
    rgblight_wakeup();
  }
}


void check_rgb_timeout() {
  if (!is_rgb_timeout && timer_elapsed(key_timer) > RGBLIGHT_TIMEOUT) {
    rgblight_suspend();
    is_rgb_timeout = true;
  }
}


void housekeeping_task_user(void) {
  #ifdef RGBLIGHT_TIMEOUT
  check_rgb_timeout();
  #endif

  if(is_keyboard_master()){
    static uint32_t last_sync = 0;
    if (timer_elapsed32(last_sync) > 500){
        if (transaction_rpc_send(RPC_OLED_SYNC, sizeof(is_rgb_timeout), &is_rgb_timeout)) {
            last_sync = timer_read32();
        }
    }
  }
}


void post_process_record_user(uint16_t keycode, keyrecord_t *record) {
  #ifdef RGBLIGHT_TIMEOUT
  if (record->event.pressed) refresh_rgb();
  #endif
}


void post_encoder_update_user(uint8_t index, bool clockwise) {
  #ifdef RGBLIGHT_TIMEOUT
  refresh_rgb();
  #endif
}


